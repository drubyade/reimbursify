// Helper to get currency by code
export const getCurrencyByCode = (code: string) => {
  return CURRENCIES.find((c) => c.code === code);
};

// Helper to get flag for a currency code
export const getFlagForCurrency = (code: string): string => {
  const currency = getCurrencyByCode(code);
  return currency ? currency.flag : "🌍";
};

// Get display string for currency (used in dropdowns)
export const getCurrencyDisplay = (code: string): string => {
  const currency = getCurrencyByCode(code);
  return currency ? `${currency.flag} ${code} - ${currency.name}` : code;
};
